#include <stdio.h>


int main(){

    // INT TO FLOAT

    int n = 10;
    float n1 = (float) n;
    printf("%f\n", n1);


    // FLOAT TO INT
    float n2 = 10.5;
    int n3 = (int) n2;
    printf("%i\n", n3);

    // TIP NEW-VARIABLE = (NEW TIPE) OLD-VARIABLE                -> FOR EVER CASE

    return 0;
}